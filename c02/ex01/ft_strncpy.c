char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;
	int	j;

	i = 0;
	j = n;
	while (src[i] != '\0' && i < j)
	{
		dest[i] = src[i];
		i++;
	}
	if (i < j)
	{
		while (i < j)
		{
			dest[i] = '\0';
			i++;
		}
	}
	return (dest);
}
