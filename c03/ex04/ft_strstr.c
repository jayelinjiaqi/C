char	*ft_strstr(char *str, char *to_find)
{
	int	i;

	i = 0;
	if (to_find == '\0')
		return (&str);
	while (str[i])
	{
		if (str[i] == *to_find)
			return (&str[i]);
		i++;
	}
	return (0);
}
